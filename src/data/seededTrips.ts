export const SEEDED_TRIPS: SeededTrip[] = RAW_SEEDED_TRIPS.map((trip) => ({
  ...trip,
  cover_image_url: getTripRequestImageUrl(trip.destination, trip.cover_image_url),
}));

export const SEEDED_TRIP_COUNT = SEEDED_TRIPS.length;

export function getSeededTripBySlug(slug?: string | null) {
  if (!slug) return null;
  return SEEDED_TRIPS.find((trip) => trip.slug === slug) ?? null;
}

export function mergeSeededTrips<T extends { slug: string }>(liveTrips: T[] | null | undefined) {
  const merged = new Map<string, T | SeededTrip>();
  SEEDED_TRIPS.forEach((trip) => merged.set(trip.slug, trip));
  (liveTrips ?? []).forEach((trip) => merged.set(trip.slug, trip));
  return Array.from(merged.values());
}
